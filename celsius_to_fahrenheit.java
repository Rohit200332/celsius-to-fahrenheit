// Java Program to Convert Celsius into Fahrenheit 
class celsius_to_fahrenheit { 
	public static void main(String[] args) 
	{ 
		
		double celsius = 10.0, fahrenheit = 0.0; 
		
		 
		fahrenheit = (celsius * 1.8) + 32; 
		System.out.println( 
			"  Temperature in Fahrenheit:"
			+ fahrenheit); 
	} 
}
